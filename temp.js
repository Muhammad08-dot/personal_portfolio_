// Map lowercase to uppercase for this simple 5x5 font
const text = "( -_^) > print(\"Hi there, let's build together!\")".toUpperCase();

const rows = ['', '', '', '', '', '', ''];
// We'll center the 5x5 font in 7 rows (padding top and bottom)
for (let r = 0; r < 7; r++) {
  for (let i = 0; i < text.length; i++) {
    const char = text[i];
    let charRows = font[char] || font[' '];
    // if r is 0 or 6, empty row unless it's a character that needs descent
    // Actually, to make it 5x7:
    if (r === 0 || r === 6) {
      if (char === '_' && r === 6) rows[r] += '11111 ';
      else if (char === ',' && r === 6) rows[r] += '01000 ';
      else if (char === '(' && r === 0) rows[r] += '00010 ';
      else if (char === '(' && r === 6) rows[r] += '00010 ';
      else if (char === ')' && r === 0) rows[r] += '01000 ';
      else if (char === ')' && r === 6) rows[r] += '01000 ';
      else rows[r] += '00000 ';
    } else {
      let fontRow = r - 1;
      let line = charRows[fontRow];
      if (char === '(') {
        if (fontRow === 0) line = '00100';
        if (fontRow === 1) line = '01000';
        if (fontRow === 2) line = '01000';
        if (fontRow === 3) line = '01000';
        if (fontRow === 4) line = '00100';
      }
      if (char === ')') {
        if (fontRow === 0) line = '00100';
        if (fontRow === 1) line = '00010';
        if (fontRow === 2) line = '00010';
        if (fontRow === 3) line = '00010';
        if (fontRow === 4) line = '00100';
      }
      rows[r] += line + ' ';
    }
  }
}

